package com.jp.capgemini;

public class Incrementacao {

    public static void main(String[] args) {

        int num = 0;
        while (num <= 9){
            num++;
            System.out.println(num);
        }
        System.out.println("----------------------------");

        int num2 = 10;
        while (num2 > 0){
            System.out.println(num2);
            num2--;
        }

    }
}
